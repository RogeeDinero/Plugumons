# It is not intended for manual editing.
version = 3

[[package]]
name = "plugumons-staking"
version = "0.1.0"
EOF

# Try to update dependencies without generating v4
echo ""
echo "ðŸ”„ Updating dependencies with version 3..."
cargo update --workspace 2>&1 | head -20

# Now try to build
echo ""
echo "ðŸ—ï¸  Building with Anchor..."
anchor build

if [ $? -eq 0 ]; then
    echo ""
    echo "âœ…âœ…âœ… SUCCESS! âœ…âœ…âœ…"
    ls -lh target/deploy/*.so
    exit 0
else
    echo ""
    echo "âŒ Still failed. Trying direct cargo build..."
    
    # Try building just the program
    cd programs/plugumons-staking
    
    # Create minimal lockfile here too
    cat > Cargo.lock << 'EOF'
# This file is automatically @generated by Cargo.
# It is not intended for manual editing.
version = 3

[[package]]
name = "plugumons-staking"
version = "0.1.0"
EOF
    
    cargo build-sbf --manifest-path Cargo.toml
    
    if [ $? -eq 0 ]; then
        echo ""
        echo "âœ… Direct build succeeded!"
        find ../../target -name "*.so"
    else
        echo ""
        echo "âŒ Direct build also failed"
        echo ""
        echo "ðŸ” The issue appears to be with Anchor/Cargo toolchain compatibility"
        echo ""
        echo "ðŸ’¡ Alternative: Build using Docker"
        echo "   docker run --rm -v \$(pwd):/workspace projectserum/build:v0.29.0 anchor build"
    fi
